    fn setup() -> (
        TempDir,
        Arc<RocksDbStore>,
        Arc<Mutex<TantivyIndex>>,
        Arc<GraphStore>,
    ) {
        let tmp = TempDir::new().unwrap();
        let store =
            Arc::new(RocksDbStore::open(tmp.path().join("rocks"), &rocks_config()).unwrap());
        let tantivy = Arc::new(Mutex::new(
            TantivyIndex::open(tmp.path().join("tantivy"), &tantivy_config()).unwrap(),
        ));
        let graph = Arc::new(GraphStore::new(store.clone()));
        (tmp, store, tantivy, graph)
    }

    #[tokio::test]
    async fn test_delete_memory_fully_soft_deletes_and_propagates() {
        let (_tmp, store, tantivy, graph) = setup();
        let chunk = make_chunk("test-id-1", "s1", "secret content");
        store.store_chunk(&chunk).unwrap();

        // Index in tantivy so we can observe the delete propagates.
        {
            let mut idx = tantivy.lock().await;
            idx.index_document(make_text_doc(&chunk)).unwrap();
            idx.commit().unwrap();
        }

        assert!(store.get_chunk("test-id-1").unwrap().is_some());

        let outcome = delete_memory_fully(&store, &tantivy, &graph, "test-id-1")
            .await
            .unwrap();
        assert!(
            outcome.store_deleted,
            "expected store_deleted=true for existing chunk"
        );
        assert!(outcome.all_ok(), "expected all steps OK for happy path");

        let after = store.get_chunk("test-id-1").unwrap().unwrap();
        assert!(
            after.deleted_at.is_some(),
            "deleted_at should be set after delete_memory_fully"
        );
    }

    #[tokio::test]
