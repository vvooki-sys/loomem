    /// Cycle/78 regression: memory_delete must remove the embedding from
    /// CF_EMBEDDINGS, not just tombstone the chunk. Pre-fix, soft-delete left
    /// the embedding in place until the ~30-day hard-purge window — which:
    /// 1. Inflated `count_embeddings()` (the global metric in memory_status)
    ///    so it never decreased after delete.
    /// 2. Polluted `vector_search`'s candidate pool (zombie embeddings got
    ///    cosine-scored alongside live ones, then dropped post-rank by the
    ///    is_deleted_in_store filter — wasting top_k slots).
    /// 3. Polluted clustering centroids (clustering loops over
    ///    `get_all_embeddings()` and only filtered by stream + is_latest,
    ///    not by deleted_at — so centroids drifted toward deleted content).
    #[tokio::test]
    async fn test_delete_memory_fully_removes_embedding_from_index() {
        let (_tmp, store, tantivy, graph) = setup();
        let chunk = make_chunk("test-id-emb", "s1", "embedding leak repro");
        store.store_chunk(&chunk).unwrap();
        store
            .store_embedding("test-id-emb", vec![0.1f32, 0.2, 0.3])
            .expect("store_embedding");

        // Pre-condition: embedding present, count == 1.
        assert_eq!(store.count_embeddings().unwrap(), 1);
        assert!(store.get_embedding("test-id-emb").unwrap().is_some());

        let outcome = delete_memory_fully(&store, &tantivy, &graph, "test-id-emb")
            .await
            .unwrap();
        assert!(
            outcome.all_ok(),
            "delete_memory_fully should report all_ok in the embedding-leak repro: {outcome:?}"
        );

        // Post-condition: embedding removed, count == 0.
        assert_eq!(
            store.count_embeddings().unwrap(),
            0,
            "embedding count must drop to 0 after memory_delete (cycle/78 leak repro)"
        );
        assert!(
            store.get_embedding("test-id-emb").unwrap().is_none(),
            "embedding must be hard-deleted from CF_EMBEDDINGS"
        );

        // Chunk is soft-deleted (content preserved for recovery), but
        // embedding is recomputable from content if needed.
        let chunk_after = store.get_chunk("test-id-emb").unwrap().unwrap();
        assert!(chunk_after.deleted_at.is_some());
        assert_eq!(chunk_after.content, "embedding leak repro");
    }

    /// Cycle/117 (a): partial-success when graph step fails. Corrupt the
    /// `graph:chunk:<id>` reverse-index bytes so `remove_chunk_references`
    /// returns Err from `get_entities_for_chunk` deserialize. The chain must
    /// still soft-delete the chunk and hard-delete the embedding (i.e. no
    /// leak), with the failure surfaced in `outcome.graph`.
    #[tokio::test]
    async fn test_delete_partial_success_graph_fails() {
        let (_tmp, store, tantivy, graph) = setup();
        let chunk = make_chunk("test-id-partial", "s1", "partial success body");
        store.store_chunk(&chunk).unwrap();
        store
            .store_embedding("test-id-partial", vec![0.1f32, 0.2, 0.3])
            .expect("store_embedding");

        // Poison the graph chunk reverse-index with malformed JSON. This
        // bypasses fail-graceful (which only covers entity reads) and forces
        // remove_chunk_references → get_entities_for_chunk to return Err.
        store
            .put(b"graph:chunk:test-id-partial", b"\xff\xfe not valid json")
            .expect("put corrupted chunk index");

        let outcome = delete_memory_fully(&store, &tantivy, &graph, "test-id-partial")
            .await
            .expect("delete_memory_fully should return outcome, not Err");

        // Store + tantivy + embedding succeeded; graph step is the failure.
        assert!(outcome.store_deleted, "chunk existed → store_deleted=true");
        assert!(outcome.store.is_ok(), "store step OK");
        assert!(outcome.tantivy.is_ok(), "tantivy step OK");
        assert!(outcome.embedding.is_ok(), "embedding step OK (no leak)");
        assert!(
            outcome.graph.is_err(),
            "graph step must fail on corrupted chunk reverse-index"
        );
        assert!(
            !outcome.all_ok(),
            "all_ok() must be false when graph step failed"
        );

        // Critical post-condition: embedding is GONE despite graph failure.
        // Pre-fix (fail-fast), the embedding would remain because the chain
        // short-circuited at graph. This is the cycle/117 embedding-leak fix.
        assert!(
            store.get_embedding("test-id-partial").unwrap().is_none(),
            "embedding must be hard-deleted even when graph step failed"
        );
        assert_eq!(store.count_embeddings().unwrap(), 0);

        // Chunk is still soft-deleted.
        let chunk_after = store.get_chunk("test-id-partial").unwrap().unwrap();
        assert!(chunk_after.deleted_at.is_some());
    }

    /// Cycle/117 (b): happy path returns DeleteOutcome with all steps OK.
    #[tokio::test]
