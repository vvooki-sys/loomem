    #[test]
    fn test_deleted_at_semantics_match_is_deleted_contract() {
        // is_deleted_in_store requires an AppState which is heavy to build
        // in-test. Exercise the store pathway directly to assert the
        // fail-closed contract captured by is_deleted_in_store.
        let tmp = TempDir::new().unwrap();
        let store = RocksDbStore::open(tmp.path().join("rocks"), &rocks_config()).unwrap();

        // Missing id → Ok(None) → treated as deleted.
        assert!(matches!(store.get_chunk("never-inserted"), Ok(None)));

        // Present, not deleted → deleted_at is None.
        let chunk = make_chunk("present", "s1", "hello");
        store.store_chunk(&chunk).unwrap();
        let got = store.get_chunk("present").unwrap().unwrap();
        assert!(got.deleted_at.is_none());

        // Soft-deleted → deleted_at is Some.
        store.delete_by_id("present").unwrap();
        let got2 = store.get_chunk("present").unwrap().unwrap();
        assert!(got2.deleted_at.is_some());
    }
