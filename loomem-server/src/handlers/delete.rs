//! Shared delete propagation helper.
//!
//! Every delete entry point (admin DELETE handler, MCP `tool_delete`) must
//! propagate the soft-delete across all retrieval surfaces: Tantivy FTS index
//! and graph edge store. Silent error swallowing historically produced 30-day
//! windows where deleted content stayed in BM25 → reranker (RED-SD-1/-6 from
//! /07-soft-delete-audit). This helper is the single point that enforces
//! storage + tantivy + graph cleanup with propagated errors.

use std::sync::Arc;

use anyhow::{Context, Result};
use loomem_core::graph::GraphStore;
use loomem_core::storage::RocksDbStore;
use loomem_core::tantivy_index::TantivyIndex;
use tokio::sync::Mutex;

use crate::AppState;

/// Per-step outcome of `delete_memory_fully`. Each step is independent and
/// records its own `Result`. The HTTP handler maps overall outcome to
/// 200 (all OK), 207 (partial — at least one step failed), or 404
/// (`store_deleted == false`, chunk did not exist).
///
/// Cycle/117 (delete chain reorder + partial-success): the previous fail-fast
/// chain (store → tantivy → graph → embedding) returned HTTP 500 from any
/// graph deserialize failure and left the embedding in place — embedding
/// leak. Reordered to store → tantivy → embedding → graph so the cheap
/// idempotent steps (store, tantivy, embedding) always run, and the
/// cross-reference-heavy graph step is last + non-fatal.
#[must_use = "DeleteOutcome captures partial-failure state — discarding it silently swallows tantivy/embedding/graph errors (cycle/124)"]
#[derive(Debug)]
pub struct DeleteOutcome {
    /// True iff `store.delete_by_id` reported the chunk existed. Maps to
    /// HTTP 404 when false.
    pub store_deleted: bool,
    /// Always `Ok(())` — fatal store errors are surfaced as `Err` returns
    /// from `delete_memory_fully` (the `?` on `store.delete_by_id`), never
    /// captured here. Retained for API-shape symmetry with the other per-step
    /// fields; the HTTP 207 response body therefore always shows
    /// `"store": "ok"`. Structural removal would change the response shape
    /// — out of scope for /124. (cycle/124 P2 #1, Greptile review on PR #191.)
    pub store: Result<()>,
    pub tantivy: Result<()>,
    pub embedding: Result<()>,
    pub graph: Result<()>,
}

impl DeleteOutcome {
    /// True when every step succeeded. False when at least one step failed.
    pub fn all_ok(&self) -> bool {
        self.store.is_ok() && self.tantivy.is_ok() && self.embedding.is_ok() && self.graph.is_ok()
    }
}

/// Soft-delete a chunk across all retrieval surfaces.
///
/// Calls, in order: `store.delete_by_id` (RocksDB soft-delete),
/// `tantivy.delete_document` (FTS tombstone), `store.delete_embedding`
/// (vector index cleanup), `graph.remove_chunk_references` (edge cleanup).
/// All four ops are idempotent — on partial failure the caller can retry
/// and reach a consistent state.
///
/// Returns `DeleteOutcome` capturing per-step results. The handler decides
/// HTTP status from the outcome (200 / 207 / 404). The function itself
/// only returns `Err` if `store.delete_by_id` (the first step, which
/// determines existence) fails fatally; tantivy/embedding/graph step
/// failures are captured inside the outcome and do NOT short-circuit
/// the chain — this is the cycle/117 fix for the embedding leak.
///
/// Embedding hard-delete rationale (cycle/78 fix): the chunk itself is
/// soft-deleted (sets `deleted_at`, content preserved for the 30-day
/// recovery window); the embedding is hard-deleted because it is a derived
/// index of `chunk.content` and can be regenerated by the embedding queue
/// if the chunk is later recovered.
///
/// Rollback strategy on failure: each step is idempotent; the caller can
/// retry the whole call and the steps that already succeeded will no-op.
pub async fn delete_memory_fully(
    store: &RocksDbStore,
    tantivy: &Mutex<TantivyIndex>,
    graph: &GraphStore,
    id: &str,
) -> Result<DeleteOutcome> {
    let store_deleted = store
        .delete_by_id(id)
        .context("store.delete_by_id failed")?;

    let tantivy_result = {
        let mut idx = tantivy.lock().await;
        idx.delete_document(id)
    };
    if let Err(err) = tantivy_result.as_ref() {
        tracing::error!(
            id = %id,
            error = %err,
            "tantivy.delete_document failed after store.delete_by_id succeeded — orphan detected, retry required"
        );
    }

    let embedding_result = store.delete_embedding(id);
    if let Err(err) = embedding_result.as_ref() {
        tracing::error!(
            id = %id,
            error = %err,
            "store.delete_embedding failed after store + tantivy delete — embedding leak, retry required"
        );
    }

    let graph_result = graph.remove_chunk_references(id);
    if let Err(err) = graph_result.as_ref() {
        tracing::error!(
            id = %id,
            error = %err,
            "graph.remove_chunk_references failed after store + tantivy + embedding delete — edges stale, retry required"
        );
    }

    Ok(DeleteOutcome {
        store_deleted,
        store: Ok(()),
        tantivy: tantivy_result
            .map_err(|e| e.context("tantivy.delete_document failed after store soft-delete")),
        embedding: embedding_result
            .map_err(|e| e.context("store.delete_embedding failed after store + tantivy delete")),
        graph: graph_result.map_err(|e| {
            e.context(
                "graph.remove_chunk_references failed after store + tantivy + embedding delete",
            )
        }),
    })
}

/// Post-retrieval soft-delete guard. Returns true when the chunk is missing,
/// unreadable, or has `deleted_at` set — treats unknown/error states as deleted
/// (fail-closed for privacy, per /07-soft-delete-fix brief Krok 3b).
pub fn is_deleted_in_store(state: &Arc<AppState>, id: &str) -> bool {
    match state.store.get_chunk(id) {
        Ok(Some(chunk)) => chunk.deleted_at.is_some(),
        Ok(None) | Err(_) => true,
    }
}

